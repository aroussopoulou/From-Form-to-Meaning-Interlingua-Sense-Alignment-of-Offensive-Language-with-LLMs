  batch_idx = indices_ar[start:stop]

  for idx in tqdm(batch_idx, desc=f"Rows {start}-{stop-1}", leave=False, unit="row"):
    # Definition
    if pd.isna(df_ar.at[idx, "Definition_en"]):
      df_ar.at[idx, "Definition_en"] = translate(df_ar.at[idx, "Definition"])

    # Example
    if pd.isna(df_ar.at[idx, "Example_en"]):
      df_ar.at[idx, "Example_en"] = translate(df_ar.at[idx, "Example"])

    # Lemma head-word
    if pd.isna(df_ar.at[idx, "Lemma_en"]):
      df_ar.at[idx, "Lemma_en"] = headword(df_ar.at[idx, "Lemma"], df_ar.at[idx, "Definition_en"])

    time.sleep(0.2)

    # save after each batch
  df_ar.to_csv(output_path_ar, index=False, encoding="utf-8")
  print(f"✅ Saved rows {start}–{stop-1}")

