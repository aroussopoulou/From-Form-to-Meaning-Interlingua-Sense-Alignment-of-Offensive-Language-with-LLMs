def translate(text: str) -> str:
  """Arabic → English literal translation (no explanations)."""
  text = str(text).strip()
  if not text:
    return ""
  renspond = client.chat.completions.create(
    model="gpt-4.1-mini",
    temperature=0,
    messages=[
      {"role": "system",
        "content": ("You are a professional translator from Arabic to English. "
                    "Translate the sentence exactly, preserving meaning and tone. "
                    "Return ONLY the translation—no comments.")},
      {"role": "user", "content": text},
    ],)
  return renspond.choices[0].message.content.strip()

def headword(lemma: str, definition: str) -> str:
  """Return one English head-word for the given Arabic lemma & definition."""
  lemma = str(lemma).strip()
  definition = str(definition).strip()
  if not lemma:
    return ""
  prompt = (f"You are a bilingual lexicographer.\n"
      f"Arabic lemma: {lemma}\n"
      f"English gloss/definition: {definition}\n"
      "Respond with ONE English head-word only.")
  renspond = client.chat.completions.create(
      model="gpt-4.1-mini",
      temperature=0,
      messages=[{"role": "user", "content": prompt}],
    )
  return renspond.choices[0].message.content.strip().split()[0].lower()

