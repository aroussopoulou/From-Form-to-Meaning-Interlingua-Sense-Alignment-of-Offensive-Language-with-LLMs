FIELDNAMES = ["lemma", "pos", "category","definition_el","definition_en",
              "synonyms_gr","synonyms_en","examples"]


def extract_json(raw: str) -> str:
    # Βγάζει έξω μόνο το JSON array, αν υπάρχει μέσα σε fences ή άλλο περιβάλλον
    m = re.search(r"(\[\s*\{.*\}\s*\])", raw, re.DOTALL)
    return m.group(1) if m else raw

def save_all_rows(all_rows: list[dict], csv_path: str):
    out = pathlib.Path(csv_path)
    out.parent.mkdir(parents=True, exist_ok=True)
    with out.open("w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, FIELDNAMES)
        w.writeheader()
        w.writerows(all_rows)
    print(f"✓ saved TOTAL {len(all_rows)} rows → {csv_path}")

